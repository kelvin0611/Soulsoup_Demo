export function getSoupsByConstitution(constitutionId: string): SoupPack[] {
  const specific = soupPacks.filter(soup => 
    soup.suitableFor.includes(constitutionId)
  )
  
  if (constitutionId !== 'balanced') {
    const general = soupPacks.filter(soup => 
      soup.suitableFor.includes('balanced') && !soup.suitableFor.includes(constitutionId)
    )
    return [...specific, ...general].slice(0, 4)
  }
  
  return specific.slice(0, 4)
}
